              <strong>Profil Wilayah</strong> <br />
        <span style="padding-left:50px"></span>Kota Kediri ”berumah” nan jauh arah barat daya Ibu Kota Provinsi Jawa Timur, Surabaya. Jarak dari Kota Pahlawan sekitar 130 km. Untuk catatan jumlah penduduk, Kota Kediri adalah kota terbesar nomor 3 (tiga) di Jawa Timur. Kota nomor satu diduduki Surabaya. Disusul di nomor urut 2 (dua) Kota Malang. Menurut Badan Pusat Statistik (BPS) Jawa Timur, sampai 2015 penduduk Kota Kediri berjumlah 312.999 orang/jiwa. Seluruh wilayah kota ibarat dalam kepungan Kabupaten Kediri. Ini karena seluruh wilayahnya berbatasan dengan Kabupaten Kediri. Baik sebelah utara, barat, selatan, maupun timur berbatasan dengan Kabupaten Kediri. Kota Kediri juga terbelah oleh sungai tua dengan histori dan heroisme besar. Kali Brantas.
              <br>
                            
            <strong>Posisi Geografis</strong> <br />
        <span style="padding-left:50px"></span>Kota Kediri eksis pada posisi antara 111º15´ – 112º03´ Bujur Timur dan 7º45´ – 7º55´ Lintang Selatan. Adapun dari aspek topografi, Kota Kediri terletak pada ketinggian rata-rata 67 meter di atas permukaan laut. Tingkat kemiringannya 0-40 persen.
              <br><br>
              <strong>Luas Kota</strong> <br />
        <span style="padding-left:50px"></span>Sebagai wilayah perkotaan, Kota Kediri tercatat tidak terlalu luas. Sampai saat ini (2016) total luas kota hanya 63,40 km2 atau 6,340 hektare (ha).
