              <p>
              <span style="padding-left:50px"></span>Artefak arkeologi yang ditemukan pada tahun 2007 menunjukkan bahwa daerah sekitar Kediri menjadi lokasi kerajaan Kediri, sebuah kerajaan Hindu pada abad ke-11. Awal mula Kediri sebagai pemukiman perkotaan dimulai ketika Airlangga memindahkan pusat pemerintahan kerajaannya dari Kahuripan ke Dahanapura, menurut Serat Calon Arang. Dahanapura (Kota Api) selanjutnya lebih dikenal sebagai Daha. Sepeninggal Raja Airlangga, wilayah Medang dibagi menjadi dua: Panjalu di barat dan Janggala di timur. Daha menjadi pusat pemerintahan Kerajaan Panjalu dan Kahuripan menjadi pusat pemerintahan Kerajaan Jenggala. Panjalu oleh penulis-penulis periode belakangan juga disebut sebagai Kerajaan Kadiri/Kediri.
              <br><br><span style="padding-left:50px"></span>Semenjak Kerajaan Tumapel (Singasari) menguat, ibukota Daha diserang dan kota ini menjadi kedudukan raja vazal, yang terus berlanjut hingga Majapahit, Demak, dan Mataram. Kediri jatuh ke tangan VOC sebagai konsekuensi Geger Pecinan. Jawa Timur pada saat itu dikuasai Cakraningrat IV, adipati Madura yang memihak VOC dan menginginkan bebasnya Madura dari Kasunanan Kartasura. Karena Cakraningrat IV keinginannya ditolak oleh VOC, ia memberontak. Pemberontakannya ini dikalahkan VOC, dibantu Pakubuwana II, sunan Kartasura. Sebagai pembayaran, Kediri menjadi bagian yang dikuasai VOC. Kekuasaan Belanda atas Kediri terus berlangsung sampai Perang Kemerdekaan Indonesia.
              <br><br><span style="padding-left:50px">Perkembangan Kota Kediri menjadi swapraja dimulai ketika diresmikannya Gemeente Kediri pada tanggal 1 April 1906 berdasarkan Staasblad (Lembaran Negara) no. 148 tertanggal 1 Maret 1906. Gemeente ini menjadi tempat kedudukan Residen Kediri dengan sifat pemerintahan otonom terbatas dan mempunyai Gemeente Raad (Dewan Kota/DPRD) sebanyak 13 orang, yang terdiri dari delapan orang golongan Eropa dan yang disamakan (Europeanen), empat orang Pribumi (Inlanders) dan satu orang Bangsa Timur Asing. Sebagai tambahan, berdasarkan Staasblad No. 173 tertanggal 13 Maret 1906 ditetapkan anggaran keuangan sebesar f. 15.240 dalam satu tahun. Baru sejak tanggal 1 Nopember 1928 berdasarkan Stbl No. 498 tanggal 1 Januari 1928, Kota Kediri menjadi "Zelfstanding Gemeenteschap" ("kota swapraja" dengan menjadi otonomi penuh).
